# 赋值people、cat、dog三个函数
people = 20
cats = 30
dogs = 15

# 如果people小于cat，则输出
if people < cats:
    print("Too many cats! The world is doomed!")

# 如果people大于cat，则输出
if people > cats:
    print("Not many cats! The world is saved!")

# 如果people小于dog，则输出
if people < dogs:
    print("The world is drooled on!")

# 如果people大于dog，则输出
if people > dogs:
    print("The world is dry!")

# 给dog的赋值递增
dogs += 5

# 如果people大于等于dog，则输出
if people >= dogs:
    print("People are greater than or equal to dogs.")

# 如果people小于等于dog，则输出
if people <= dogs:
    print("People are less than or equal to dogs.")

# 如果people等于dog，则输出
if people == dogs:
    print("People are dogs")